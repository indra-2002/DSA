# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
from collections import deque
class Solution:
    def zigzagLevelOrder(self, root: Optional[TreeNode]) -> List[List[int]]:
        if not root:
            return []
        flag=0
        q=deque([root])
        res=[]
        while q:

            l=[]
            for _ in range(len(q)):
                node=q.popleft()
                l.append(node.val)
                

                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)

            if flag%2==1:
                res.append(l[::-1])
                
            else:
                res.append(l)
            flag+=1
        return res